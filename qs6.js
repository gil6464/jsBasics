let David = 80;
let Vinoth = 77;
let Divya = 88;
let Ishitha = 95;
let Thomas = 68;
let avg = (( David + Vinoth + Divya + Ishitha + Thomas ) / 5 );
let grade = "";
if ( avg < 60 ) {
     grade = "F"
} else if ( avg < 70 ) {
     grade = "D" 
} else if ( avg < 80 ) {
     grade = "C"
} else if ( avg < 90 ) {
     grade = "B"
} else {
     grade = "A"
}
